# -*- coding: utf-8 -*-
"""
Arquivo de Exemplo: Operadores Aritméticos em Python

Este arquivo demonstra o uso dos operadores aritméticos básicos em Python.

1. Adição (+)
2. Subtração (-)
3. Multiplicação (*)
4. Divisão (/)
5. Divisão Inteira (//)
6. Resto da Divisão (%)
7. Exponenciação (**)

"""

# 1. Adição
soma = 5 + 3
print(f"1. Adição: 5 + 3 = {soma}")

# 2. Subtração
subtracao = 10 - 7
print(f"2. Subtração: 10 - 7 = {subtracao}")

# 3. Multiplicação
multiplicacao = 4 * 6
print(f"3. Multiplicação: 4 * 6 = {multiplicacao}")

# 4. Divisão
divisao = 15 / 3
print(f"4. Divisão: 15 / 3 = {divisao}")

# 5. Divisão Inteira
divisao_inteira = 18 // 5
print(f"5. Divisão Inteira: 17 // 5 = {divisao_inteira}")

# 6. Resto da Divisão
resto_divisao = 17 % 5
print(f"6. Resto da Divisão: 17 % 5 = {resto_divisao}")

# 7. Exponenciação
exponenciacao = 2 ** 4
print(f"7. Exponenciação: 2 ** 4 = {exponenciacao}")
