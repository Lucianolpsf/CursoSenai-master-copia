# -*- coding: utf-8 -*-
"""
Arquivo de Exemplo: Operadores Relacionais em Python

Este arquivo demonstra o uso dos operadores relacionais básicos em Python.

1. Igualdade (==)
2. Desigualdade (!=)
3. Maior que (>)
4. Menor que (<)
5. Maior ou igual a (>=)
6. Menor ou igual a (<=)

"""

# 1. Igualdade (==)
idade = 25

if idade == 25:
    print("1. A idade é igual a 25.")
else:
    print("1. A idade não é igual a 25.")

# 2. Desigualdade (!=)
nota = 8

if nota != 10:
    print("2. A nota não é 10.")
else:
    print("2. A nota é 10.")

# 3. Maior que (>)
valor = 15

if valor > 10:
    print("3. O valor é maior que 10.")
else:
    print("3. O valor não é maior que 10.")

# 4. Menor que (<)
quantidade = 5

if quantidade < 10:
    print("4. A quantidade é menor que 10.")
else:
    print("4. A quantidade não é menor que 10.")

# 5. Maior ou igual a (>=)
numero = 7

if numero >= 5:
    print("5. O número é maior ou igual a 5.")
else:
    print("5. O número não é maior ou igual a 5.")

# 6. Menor ou igual a (<=)
saldo = 1000

if saldo <= 1500:
    print("6. O saldo é menor ou igual a 1500.")
else:
    print("6. O saldo não é menor ou igual a 1500.")
